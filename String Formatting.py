# f-Strings
name = "Ali"
age = 20
print(f"My name is {name} and I am {age} years old.")


# format() Method
name = "Sara"
marks = 95
print("Student: {}, Marks: {}".format(name, marks))

print("Marks: {1}, Name: {0}".format(name, marks))


# % Operator (Old Style)
course = "Python"
duration = 3
print("Course: %s, Duration: %d months" % (course, duration))