#If
age = 20  

# If condition checks whether age is greater than or equal to 18
if age >= 18:  
    print("You can vote")   # This will run because age = 20, which is >= 18


#If-Else
age = 16  

# If condition checks age
if age >= 18:  
    print("You can vote")   # Runs only if age >= 18
else:  
    print("You cannot vote")  # Runs when condition is False