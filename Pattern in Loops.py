#Right Angle Triangle (Stars)
for i in range(1, 6):      
    for j in range(1, i+1):
        print("*", end=" ")
    print()
print()

# Square Pattern
for i in range(5):
    for j in range(5):
        print("*", end=" ")
    print()
print()

# Number Pattern
for i in range(1, 6):
    for j in range(1, i+1):
        print(j, end=" ")
    print()
print()

# Reverse Triangle
for i in range(5, 0, -1):
    for j in range(1, i+1):
        print("*", end=" ")
    print()