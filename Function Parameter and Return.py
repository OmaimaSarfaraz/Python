# Function with Parameter
def greet(name):
    print("Hello", name, "Welcome to Python!")

greet("Ali")
greet("Sara")


# Function with Multiple Parameters
def add(a, b):
    print("Sum is:", a + b)

add(5, 3)
add(10, 7)


# Function with Return Value
def square(x):
    return x * x

result = square(4)
print("Square is:", result)